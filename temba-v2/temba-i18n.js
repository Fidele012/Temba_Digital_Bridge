/* ──────────────────────────────────────────────────
   TRANSLATION ENGINE
   ────────────────────────────────────────────────── */
let currentLang = localStorage.getItem(LANG_KEY) || 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem(LANG_KEY, lang);
  applyTranslations();
  syncLangButtons();
}

function getLang() {
  return currentLang;
}

function t(key) {
  return T[currentLang]?.[key] || T['en']?.[key] || key;
}

function applyTranslations() {
  // Translate all [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      if (el.hasAttribute('placeholder')) el.placeholder = val;
    } else if (el.tagName === 'OPTION') {
      el.textContent = val;
    } else {
      // Allow HTML (for hero h1 with <span>)
      el.innerHTML = val;
    }
  });

  // Translate [data-i18n-placeholder] for inputs
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });

  // Update html lang attribute
  document.documentElement.lang = currentLang === 'rw' ? 'rw' : 'en';
}

